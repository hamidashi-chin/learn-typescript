let num1 = 10;
let num2 = 20;

/**
 * 足し算
 */
let test3_3_result = num1 + num2;
console.log('足し算', test3_3_result);

/**
 * 引き算
 */
test3_3_result = num1 - num2;
console.log('引き算', test3_3_result);

/**
 * 掛け算
 */
test3_3_result = num1 * num2;
console.log('掛け算', test3_3_result);

/**
 * 割り算
 */
test3_3_result = num1 / num2;
console.log('割り算', test3_3_result);